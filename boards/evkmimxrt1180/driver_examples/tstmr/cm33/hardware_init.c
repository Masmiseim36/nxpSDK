/*${function:start}*/
void BOARD_InitHardware(void)
{
    BOARD_InitBootPins();
    BOARD_BootClockRUN();
    BOARD_InitDebugConsole();
    
    /* Enable system counter */
    SYS_CTR_CONTROL->CNTCR |= 0x01;
}
