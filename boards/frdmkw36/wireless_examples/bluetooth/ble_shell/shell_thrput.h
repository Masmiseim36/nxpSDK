/*! *********************************************************************************
 * \brief  Signals that the MTU Exchange procedure has been completed.
 *
 * \remarks This function is executed in the context of the Application Task.
 *
 * \param[in]   serverDeviceId  Peer device ID
 ********************************************************************************** */
