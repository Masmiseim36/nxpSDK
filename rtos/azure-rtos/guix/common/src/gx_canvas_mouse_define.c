#if defined(GX_MOUSE_SUPPORT)
UINT _gx_canvas_mouse_define(GX_CANVAS *canvas, GX_MOUSE_CURSOR_INFO *info)
{
GX_DISPLAY *display = canvas -> gx_canvas_display;

    if (info != GX_NULL)
    {
        display -> gx_display_mouse_define(display, canvas, info);
        return GX_SUCCESS;
    }

    return GX_FAILURE;
}
#endif

