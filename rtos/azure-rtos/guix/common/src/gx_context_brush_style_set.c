UINT _gx_context_brush_style_set(UINT style)
{
GX_DRAW_CONTEXT *context = _gx_system_current_draw_context;

    /* Set the brush.  */
    context -> gx_draw_context_brush.gx_brush_style = style;

    /* Return successful completion.  */
    return(GX_SUCCESS);
}

