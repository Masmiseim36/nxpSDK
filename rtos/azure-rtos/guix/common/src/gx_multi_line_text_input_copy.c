/*                                                                        */
/*  FUNCTION                                               RELEASE        */
/*                                                                        */
/*    _gx_multi_line_text_input_copy                     PORTABLE C       */
/*                                                           6.1          */
/*  AUTHOR                                                                */
/*                                                                        */
/*    Kenneth Maxwell, Microsoft Corporation                              */
/*                                                                        */
/*  DESCRIPTION                                                           */
/*                                                                        */
/*    This function copies the highlighted text.                          */
/*                                                                        */
/*  INPUT                                                                 */
/*                                                                        */
/*     input                                Multi-line text input widget  */
/*                                            control block               */
/*                                                                        */
/*  OUTPUT                                                                */
/*                                                                        */
/*    None                                                                */
/*                                                                        */
/*  CALLS                                                                 */
/*                                                                        */
/*    _gx_system_clipboard_put             Put data to clipboard          */
/*                                                                        */
/*  CALLED BY                                                             */
/*                                                                        */
/*    GUIX Internal Code                                                  */
/*                                                                        */
/*  RELEASE HISTORY                                                       */
/*                                                                        */
/*    DATE              NAME                      DESCRIPTION             */
/*                                                                        */
/*  05-19-2020     Kenneth Maxwell          Initial Version 6.0           */
/*  09-30-2020     Kenneth Maxwell          Modified comment(s),          */
/*                                            resulting in version 6.1    */
/*                                                                        */
/**************************************************************************/
UINT _gx_multi_line_text_input_copy(GX_MULTI_LINE_TEXT_INPUT *input)
{
UINT start_mark = input -> gx_multi_line_text_input_start_mark;
UINT end_mark = input -> gx_multi_line_text_input_end_mark;

    if (start_mark != end_mark)
    {
        if (start_mark > end_mark)
        {
            GX_SWAP_VALS(start_mark, end_mark);
        }

        _gx_system_clipboard_put((VOID *)(input -> gx_multi_line_text_view_text.gx_string_ptr + start_mark), end_mark - start_mark);
    }

    return GX_SUCCESS;
}

