UINT  _gx_rich_text_view_context_pop()
{
    if (_gx_system_rich_text_context_stack.gx_rich_text_context_stack_top <= 0)
    {
        return GX_FAILURE;
    }

    _gx_system_rich_text_context_stack.gx_rich_text_context_stack_top--;

    return GX_SUCCESS;
}

