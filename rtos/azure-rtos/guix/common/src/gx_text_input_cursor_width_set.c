UINT _gx_text_input_cursor_width_set(GX_TEXT_INPUT_CURSOR *cursor_input, GX_UBYTE width)
{
    /* change the width value */
    cursor_input -> gx_text_input_cursor_width = width;

    /* Return successful status.  */
    return(GX_SUCCESS);
}

