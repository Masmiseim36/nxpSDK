/*    This function updates the pointer to the event processing           */
/*    function for the specified widget.                                  */
/*                                                                        */
/*  INPUT                                                                 */
/*                                                                        */
/*    widget                                Widget control block          */
/*    event_processing_function             pointer to event function     */
/*                                                                        */
