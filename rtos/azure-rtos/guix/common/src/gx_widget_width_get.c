UINT _gx_widget_width_get(GX_WIDGET *widget, GX_VALUE *return_width)
{

    /* Yes, return the widget's width.  */
    *return_width = (GX_VALUE)(widget -> gx_widget_size.gx_rectangle_right - widget -> gx_widget_size.gx_rectangle_left + 1);

    return(GX_SUCCESS);
}

