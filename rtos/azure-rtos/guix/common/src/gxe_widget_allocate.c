/*    This function allocates memory for a widget control block           */
/*                                                                        */
/*  INPUT                                                                 */
/*                                                                        */
/*    widget                                Return pointer for widget     */
/*                                            control block.              */
/*    memsize                               Size of control block.        */
/*                                                                        */
/*  OUTPUT                                                                */
/*                                                                        */
/*    status                                Completion status             */
/*                                                                        */
/*  CALLS                                                                 */
/*                                                                        */
/*    _gx_widget_allocate                   Actual widget allocation call */
/*                                                                        */
