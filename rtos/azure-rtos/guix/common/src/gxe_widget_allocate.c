UINT  _gxe_widget_allocate(GX_WIDGET **widget, ULONG memsize)
{
    if (!_gx_system_memory_allocator)
    {
        return GX_SYSTEM_MEMORY_ERROR;
    }

    if (!widget)
    {
        return GX_PTR_ERROR;
    }
    if (!memsize)
    {
        return GX_INVALID_MEMORY_SIZE;
    }
    return _gx_widget_allocate(widget, memsize);
}

