UINT  _gxe_widget_free(GX_WIDGET *widget)
{
    if (!widget)
    {
        return GX_PTR_ERROR;
    }
    if (_gx_system_memory_free == GX_NULL)
    {
        return GX_SYSTEM_MEMORY_ERROR;
    }
    return _gx_widget_free(widget);
}

